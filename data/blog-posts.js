/**
 * Search blog posts
 * @param {string} query
 * @returns {import('../types/blog.js').BlogPost[]}
 */
export function searchBlogPosts(query) {
  if (!query || query.trim() === "") {
    return blogPosts
  }

  const searchTerm = query.toLowerCase().trim()

  return blogPosts.filter((post) => {
    // Search in title
    if (post.title.toLowerCase().includes(searchTerm)) return true

    // Search in excerpt
    if (post.excerpt?.toLowerCase().includes(searchTerm)) return true

    // Search in tags
    if (post.tags?.some((tag) => tag.toLowerCase().includes(searchTerm))) return true

    // Search in categories
    if (post.categories?.some((category) => category.toLowerCase().includes(searchTerm))) return true

    // Search in content
    if (
      post.content?.some((component) => {
        if (component.content?.toLowerCase().includes(searchTerm)) return true
        if (component.title?.toLowerCase().includes(searchTerm)) return true
        return false
      })
    )
      return true

    return false
  })
}

/**
 * Get posts by category
 * @param {string} category
 * @returns {import('../types/blog.js').BlogPost[]}
 */
export function getPostsByCategory(category) {
  return blogPosts.filter((post) =>
    post.categories?.some(
      (cat) =>
        cat.toLowerCase() === category.toLowerCase() ||
        cat.toLowerCase().replace(/\s+/g, "-") === category.toLowerCase(),
    ),
  )
}

/**
 * Get all categories
 * @returns {string[]}
 */
export function getAllCategories() {
  const categories = new Set()
  blogPosts.forEach((post) => {
    if (post.categories) {
      post.categories.forEach((category) => categories.add(category))
    }
  })
  return Array.from(categories)
}

/**
 * Get all tags
 * @returns {string[]}
 */
export function getAllTags() {
  const tags = new Set()
  blogPosts.forEach((post) => {
    if (post.tags) {
      post.tags.forEach((tag) => tags.add(tag))
    }
  })
  return Array.from(tags)
}
